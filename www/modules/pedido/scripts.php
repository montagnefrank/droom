    //              OBTENEMOS LOS VALORES UNICOS DE UN ARRAY            ////
    function uniqueArray(arr) {
        var a = [];
        for (var i = 0, l = arr.length; i < l; i++)
            if (a.indexOf(arr[i]) === -1 && arr[i] !== '')
                a.push(arr[i]);
        return a;
    }
